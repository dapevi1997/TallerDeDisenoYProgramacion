package org.sokfa.tallerdedisenoyprogramacion;

/**
 * Creación de la interfaz IPreparationManned para naves tripuladas.
 *
 * @author DANIEL PEREZ VITOLA
 */
public interface IPreparationManned {

    /**
     * Método para pedir al usuario el nombre del o los tripulantes.
     */
    void crewMan();

    /**
     * Método para mostrar el nombre de los tripulantes durante el lanzamiento
     * de la nave tripulada.
     */
    void showCrewman();

}
